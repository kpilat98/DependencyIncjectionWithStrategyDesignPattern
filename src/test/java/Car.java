public class Car {
    String type;
    String fuel;
    int mass;
    int brakes;
    float emission;
    int axleNumber;

    public Car(String type,String fuel,int mass,int brakes,float emission,int axleNumber){
        this.type=type;
        this.mass=mass;
        this.fuel=fuel;
        this.brakes=brakes;
        this.emission=emission;
        this.axleNumber=axleNumber;

    }
}